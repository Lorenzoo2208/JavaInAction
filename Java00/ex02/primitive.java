package JavaInAction.Java00.ex02;

public class primitive {
    public static void main(String[] args) {
        int myInt = 42;
        double myDouble = 3.14;
        char myChar = 'A';
        boolean myBool = true;
        byte myByte = 10;
        short myShort = 3000;
        long myLong = 123456789L;
        float myFloat = 2.718f;

        System.out.println(
            "myInt :" + myInt + "\n" +
            "myDouble :" + myDouble + "\n" +
            "MyChar :" + myChar + "\n" +
            "myBoll :" + myBool + "\n" +
            "myByte :" + myByte + "\n" +
            "myShort :" + myShort + "\n" +
            "myLong :" + myLong + "\n" +
            "myFloat :" + myFloat + "\n"
            );

    }
}
